import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import { contractLiterals, fixtures, generatedTypes } from '../contracts/openai_chat/source.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, '..');
const checkMode = process.argv.includes('--check');

const generatedTypePath = resolve(
  repoRoot,
  'frontend/src/types/generated/openaiChat.ts',
);
const generatedGoPath = resolve(
  repoRoot,
  'go/internal/gateway/generated_openai_chat_contract.go',
);
const fixturesDir = resolve(repoRoot, 'contracts/openai_chat');

const outputs = [
  {
    path: generatedTypePath,
    content: renderGeneratedTypes(),
  },
  {
    path: generatedGoPath,
    content: renderGeneratedGoConstants(),
  },
  ...Object.entries(fixtures)
    .sort(([left], [right]) => left.localeCompare(right))
    .map(([name, value]) => ({
      path: resolve(fixturesDir, name),
      content: `${JSON.stringify(value, null, 2)}\n`,
    })),
];

const drift = [];

for (const output of outputs) {
  if (checkMode) {
    const existing = await readIfExists(output.path);
    if (existing !== output.content) {
      drift.push(relative(repoRoot, output.path));
    }
    continue;
  }

  await mkdir(dirname(output.path), { recursive: true });
  await writeFile(output.path, output.content, 'utf8');
  console.log(`updated ${relative(repoRoot, output.path)}`);
}

if (checkMode) {
  if (drift.length > 0) {
    console.error('OpenAI chat contract outputs are out of date:');
    for (const path of drift) {
      console.error(`- ${path}`);
    }
    process.exitCode = 1;
  } else {
    console.log('OpenAI chat contract outputs are up to date.');
  }
}

function renderGeneratedTypes() {
  return `// Code generated by scripts/generate-openai-chat-contracts.mjs.\n// Source: contracts/openai_chat/source.mjs\n// DO NOT EDIT MANUALLY.\n\n${generatedTypes.trim()}\n`;
}

function renderGeneratedGoConstants() {
  const literalConstants = [
    ['OpenAIChatCompletionObject', contractLiterals.completionObject],
    ['OpenAIChatCompletionChunkObject', contractLiterals.completionChunkObject],
    ['OpenAIChatToolTypeFunction', contractLiterals.toolTypeFunction],
    ['OpenAIChatErrorTypeInferenceError', contractLiterals.errorTypeInferenceError],
    ['OpenAIChatErrorTypeInvalidRequest', contractLiterals.errorTypeInvalidRequest],
  ];
  const fixtureConstants = Object.keys(fixtures)
    .sort((left, right) => left.localeCompare(right))
    .map((name) => [fixtureConstantName(name), name]);
  const entries = [...literalConstants, ...fixtureConstants];
  const maxNameLength = Math.max(...entries.map(([name]) => name.length));
  const constants = entries
    .map(([name, value]) => `\t${name.padEnd(maxNameLength, ' ')} = ${JSON.stringify(value)}`)
    .join('\n');

  return `// Code generated by scripts/generate-openai-chat-contracts.mjs.\n// Source: contracts/openai_chat/source.mjs\n// DO NOT EDIT MANUALLY.\n\npackage gateway\n\nconst (\n${constants}\n)\n`;
}

function fixtureConstantName(name) {
  const basename = name
    .replace(/^chat_completion_/, '')
    .replace(/\.json$/, '')
    .split('_')
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join('');
  return `OpenAIChatFixture${basename}`;
}

async function readIfExists(path) {
  try {
    return await readFile(path, 'utf8');
  } catch (error) {
    if (error && typeof error === 'object' && 'code' in error && error.code === 'ENOENT') {
      return null;
    }
    throw error;
  }
}
